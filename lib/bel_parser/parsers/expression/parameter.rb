    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
		begin

    @buffers[:parameter] ||= parameter(prefix(nil))
    @buffers[:parameter]   = @buffers[:parameter] << value(@buffers[:ident])
  		end
		begin

    yield @buffers[:parameter]
  		end
	when 15 then
		begin

    #$stderr.puts 'accum_string "' + fc.chr + '"'
    @incomplete[:string] << data[p].ord
  		end
		begin

    #$stderr.puts 'string_end'
    string = @incomplete.delete(:string) || []
    completed = @opened && @closed
    ast_node = string(utf8_string(string), complete: completed)
    @buffers[:string] = ast_node
  		end
		begin

    yield @buffers[:string]
  		end
	when 17 then
		begin

    #$stderr.puts 'end_string'
    @closed = true
  		end
		begin

    #$stderr.puts 'string_end'
    string = @incomplete.delete(:string) || []
    completed = @opened && @closed
    ast_node = string(utf8_string(string), complete: completed)
    @buffers[:string] = ast_node
  		end
		begin

    yield @buffers[:string]
  		end
	when 14 then
		begin

    #$stderr.puts 'start_string'
    @incomplete[:string] = []
    @opened = true
  		end
		begin

    #$stderr.puts 'accum_string "' + fc.chr + '"'
    @incomplete[:string] << data[p].ord
  		end
		begin

    #$stderr.puts 'string_end'
    string = @incomplete.delete(:string) || []
    completed = @opened && @closed
    ast_node = string(utf8_string(string), complete: completed)
    @buffers[:string] = ast_node
  		end
		begin

    yield @buffers[:string]
  		end
	when 5 then
		begin

    #$stderr.puts 'end_string'
    @closed = true
  		end
		begin

    #$stderr.puts 'string_end'
    string = @incomplete.delete(:string) || []
    completed = @opened && @closed
    ast_node = string(utf8_string(string), complete: completed)
    @buffers[:string] = ast_node
  		end
		begin

    @buffers[:parameter] ||= parameter(prefix(nil))
    @buffers[:parameter]   = @buffers[:parameter] << value(@buffers[:string])
  		end
		begin

    yield @buffers[:parameter]
  		end
	end
	end
	end
	if _goto_level <= _again
	if cs == 0
		_goto_level = _out
		next
	end
	p += 1
	if p != pe
		_goto_level = _resume
		next
	end
	end
	if _goto_level <= _test_eof
	if p == eof
	  case _bel_eof_actions[cs]
	when 10 then
		begin

    ident = @incomplete.delete(:ident) || []
    completed = !ident.empty?
    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
	when 11 then
		begin

    #$stderr.puts 'eof_string'
    unless @closed
      $stderr.puts "incomplete string - why?"
    else
      $stderr.puts "complete string"
    end
  		end
	when 13 then
		begin

    #$stderr.puts 'start_string'
    @incomplete[:string] = []
    @opened = true
  		end
		begin

    #$stderr.puts 'eof_string'
    unless @closed
      $stderr.puts "incomplete string - why?"
    else
      $stderr.puts "complete string"
    end
  		end
	when 16 then
		begin

    #$stderr.puts 'end_string'
    @closed = true
  		end
		begin

    #$stderr.puts 'eof_string'
    unless @closed
      $stderr.puts "incomplete string - why?"
    else
      $stderr.puts "complete string"
    end
  		end
	  end
	end

	end
	if _goto_level <= _out
		break
	end
end
	end

      # end: ragel
          end
        end
      end
    end
  end
end

