self.bel_en_unset_ast = 1;


      # end: ragel
          end

          def each
            @buffers    = {}
            @incomplete = {}
            data        = @content.unpack('C*')
            p           = 0
            pe          = data.length
            eof         = data.length

      # begin: ragel
            
begin
	p ||= 0
	pe ||= data.length
	cs = bel_start
end

            
begin
	testEof = false
	_slen, _trans, _keys, _inds, _acts, _nacts = nil
	_goto_level = 0
	_resume = 10
	_eof_trans = 15
	_again = 20
	_test_eof = 30
	_out = 40
	while true
	if _goto_level <= 0
	if p == pe
		_goto_level = _test_eof
		next
	end
	if cs == 0
		_goto_level = _out
		next
	end
	end
	if _goto_level <= _resume
	_keys = cs << 1
	_inds = _bel_index_offsets[cs]
	_slen = _bel_key_spans[cs]
	_wide = data[p].ord
	_trans = if (   _slen > 0 && 
			_bel_trans_keys[_keys] <= _wide && 
			_wide <= _bel_trans_keys[_keys + 1] 
		    ) then
			_bel_indicies[ _inds + _wide - _bel_trans_keys[_keys] ] 
		 else 
			_bel_indicies[ _inds + _slen ]
		 end
	cs = _bel_trans_targs[_trans]
	if _bel_trans_actions[_trans] != 0
	case _bel_trans_actions[_trans]
	when 12 then
		begin

    @incomplete[:ident] << data[p].ord
  		end
	when 6 then
		begin

    #$stderr.puts 'accum_unset_keyword "' + fc.chr + '"'
    @incomplete[:unset_keyword] << data[p].ord
  		end
	when 4 then
		begin

    #$stderr.puts 'end_unset_keyword'
  		end
	when 8 then
		begin

    #$stderr.puts "unset_end"
    unless @buffers.key?(:ident)
      @buffers[:ident] = identifier(nil, complete: false)
    end
    name = @buffers.delete(:ident)

    keyword = ord_to_str(@incomplete.delete(:unset_keyword))
    unless keyword == 'UNSET'
      keyword_complete = false
    else
      keyword_complete = true
    end

    completed = keyword_complete && name.complete
    ast_node = unset(name, complete: completed)
    @buffers[:unset] = ast_node
    yield @buffers[:unset]
  		end
	when 9 then
		begin

    @incomplete[:ident] = []
  		end
		begin

    @incomplete[:ident] << data[p].ord
  		end
	when 13 then
		begin

    ident = @incomplete.delete(:ident) || []
    completed = !ident.empty?
    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
		begin

    yield @buffers[:ident]
  		end
	when 11 then
		begin

    ident = @incomplete.delete(:ident) || []
    completed = !ident.empty?
    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
		begin

    #$stderr.puts "unset_end"
    unless @buffers.key?(:ident)
      @buffers[:ident] = identifier(nil, complete: false)
    end
    name = @buffers.delete(:ident)

    keyword = ord_to_str(@incomplete.delete(:unset_keyword))
    unless keyword == 'UNSET'
      keyword_complete = false
    else
      keyword_complete = true
    end

    completed = keyword_complete && name.complete
    ast_node = unset(name, complete: completed)
    @buffers[:unset] = ast_node
    yield @buffers[:unset]
  		end
	when 2 then
		begin

    #$stderr.puts 'start_unset_keyword'
    @incomplete[:unset_keyword] = []
  		end
		begin

    #$stderr.puts 'accum_unset_keyword "' + fc.chr + '"'
    @incomplete[:unset_keyword] << data[p].ord
  		end
	when 5 then
		begin

    #$stderr.puts 'end_unset_keyword'
  		end
		begin

    #$stderr.puts "unset_end"
    unless @buffers.key?(:ident)
      @buffers[:ident] = identifier(nil, complete: false)
    end
    name = @buffers.delete(:ident)

    keyword = ord_to_str(@incomplete.delete(:unset_keyword))
    unless keyword == 'UNSET'
      keyword_complete = false
    else
      keyword_complete = true
    end

    completed = keyword_complete && name.complete
    ast_node = unset(name, complete: completed)
    @buffers[:unset] = ast_node
    yield @buffers[:unset]
  		end
	end
	end
	end
	if _goto_level <= _again
	if cs == 0
		_goto_level = _out
		next
	end
	p += 1
	if p != pe
		_goto_level = _resume
		next
	end
	end
	if _goto_level <= _test_eof
	if p == eof
	  case _bel_eof_actions[cs]
	when 14 then
		begin

    ident = @incomplete.delete(:ident) || []
    completed = !ident.empty?
    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
	when 1 then
		begin

    #$stderr.puts 'eof_unset_keyword'
  		end
	when 7 then
		begin

    #$stderr.puts 'eof_unset'
  		end
	when 10 then
		begin

    ident = @incomplete.delete(:ident) || []
    completed = !ident.empty?
    ast_node = identifier(utf8_string(ident), complete: completed)
    @buffers[:ident] = ast_node
  		end
		begin

    #$stderr.puts 'eof_unset'
  		end
	when 3 then
		begin

    #$stderr.puts 'eof_unset_keyword'
  		end
		begin

    #$stderr.puts 'end_unset_keyword'
  		end
		begin

    #$stderr.puts 'eof_unset'
  		end
	  end
	end

	end
	if _goto_level <= _out
		break
	end
end
	end

      # end: ragel
          end
        end
      end
    end
  end
end

