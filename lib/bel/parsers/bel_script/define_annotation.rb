	     loop do
	        break if _upper < _lower
	        _mid = _lower + (((_upper-_lower) >> 1) & ~1)
	        if data[p].ord < _bel_trans_keys[_mid]
	          _upper = _mid - 2
	        elsif data[p].ord > _bel_trans_keys[_mid+1]
	          _lower = _mid + 2
	        else
	          _trans += ((_mid - _keys) >> 1)
	          _break_match = true
	          break
	        end
	     end # loop
	     break if _break_match
	     _trans += _klen
	  end
	end while false
	_trans = _bel_indicies[_trans]
	cs = _bel_trans_targs[_trans]
	if _bel_trans_actions[_trans] != 0
		_acts = _bel_trans_actions[_trans]
		_nacts = _bel_actions[_acts]
		_acts += 1
		while _nacts > 0
			_nacts -= 1
			_acts += 1
			case _bel_actions[_acts - 1]
when 0 then
		begin

    @buffers[:ident] = []
  		end
when 1 then
		begin

    (@buffers[:ident] ||= []) << data[p].ord
  		end
when 2 then
		begin

    @buffers[:ident] = s(:identifier,
                         utf8_string(@buffers[:ident]))
  		end
when 3 then
		begin

    yield @buffers[:ident]
  		end
when 4 then
		begin

    @buffers[:ident] ||= []
    @buffers[:ident]   = s(:identifier,
                           utf8_string(@buffers[:ident]).sub(/\n$/, ''))
  		end
when 5 then
		begin

    @buffers[:ident] ||= []
    yield @buffers[:ident]
  		end
when 6 then
		begin

    @buffers[:string] = []
  		end
when 7 then
		begin

    (@buffers[:string] ||= []) << data[p].ord
  		end
when 8 then
		begin

    @buffers[:string] = s(:string,
                          utf8_string(@buffers[:string]))
  		end
when 9 then
		begin

    @buffers[:string] ||= []
    @buffers[:string] = s(:string,
                          utf8_string(@buffers[:string]).sub(/\n$/, ''))
  		end
when 10 then
		begin

    yield @buffers[:string]
  		end
when 11 then
		begin

    @buffers[:string] ||= []
    yield @buffers[:string]
  		end
when 12 then
		begin

    @buffers.delete(:string)
    @buffers.delete(:ident)
  		end
when 13 then
		begin

    @buffers[:list_arg] = s(:list_item, @buffers[:string])
  		end
when 14 then
		begin

    @buffers[:list_arg] = s(:list_item, @buffers[:ident])
  		end
when 15 then
		begin

    @buffers[:list] = s(:list)
  		end
when 16 then
		begin

    # Append list argument if its value is not empty.
    list_arg_value = @buffers[:list_arg].children[0].children[0]
    if list_arg_value != ''
      @buffers[:list] <<= @buffers[:list_arg]
    end
  		end
when 17 then
		begin

    #TODO: Mark @buffers[:list] as complete.
  		end
when 18 then
		begin

    #TODO: Mark @buffers[:list_arg] string as error.
    @buffers[:list_arg] = s(:list_item, @buffers[:string])
  		end
when 19 then
		begin

    #TODO: Mark @buffers[:list_arg] identifier as error.
    @buffers[:list_arg] = s(:list_item, @buffers[:ident])
  		end
when 20 then
		begin

    yield @buffers[:list]
  		end
when 21 then
		begin

    @buffers[:list] ||= s(:list)
    yield @buffers[:list]
  		end
when 22 then
		begin

    @buffers[:define_annotation] = s(:define_annotation)
  		end
when 23 then
		begin

    @buffers[:define_annotation] = s(:define_annotation,
                                     s(:keyword, @buffers[:ident]))
  		end
when 24 then
		begin

    @buffers[:define_annotation] = @buffers[:define_annotation] << s(:domain)
  		end
when 25 then
		begin

    @buffers[:define_annotation] = @buffers[:define_annotation] << s(:domain, s(:pattern))
  		end
when 26 then
		begin

    @buffers[:define_annotation] = @buffers[:define_annotation] << s(:domain, s(:url))
  		end
when 27 then
		begin

    keyword, domain              = @buffers[:define_annotation].children
    domain                       = s(:domain,
                                     domain.children[0] << @buffers[:string])
    @buffers[:define_annotation] = s(:define_annotation, keyword, domain)
  		end
when 28 then
		begin

    keyword, domain              = @buffers[:define_annotation].children
    domain                       = s(:domain,
                                     domain.children[0] << @buffers[:string])
    @buffers[:define_annotation] = s(:define_annotation, keyword, domain)
  		end
when 29 then
		begin

    keyword, domain              = @buffers[:define_annotation].children
    domain                       = s(:domain,
                                     @buffers[:list])
    @buffers[:define_annotation] = s(:define_annotation, keyword, domain)
  		end
when 30 then
		begin

    yield @buffers[:define_annotation]
  		end
			end # action switch
		end
	end
	if _trigger_goto
		next
	end
	end
	if _goto_level <= _again
	if cs == 0
		_goto_level = _out
		next
	end
	p += 1
	if p != pe
		_goto_level = _resume
		next
	end
	end
	if _goto_level <= _test_eof
	if p == eof
	__acts = _bel_eof_actions[cs]
	__nacts =  _bel_actions[__acts]
	__acts += 1
	while __nacts > 0
		__nacts -= 1
		__acts += 1
		case _bel_actions[__acts - 1]
when 4 then
		begin

    @buffers[:ident] ||= []
    @buffers[:ident]   = s(:identifier,
                           utf8_string(@buffers[:ident]).sub(/\n$/, ''))
  		end
when 5 then
		begin

    @buffers[:ident] ||= []
    yield @buffers[:ident]
  		end
when 9 then
		begin

    @buffers[:string] ||= []
    @buffers[:string] = s(:string,
                          utf8_string(@buffers[:string]).sub(/\n$/, ''))
  		end
when 11 then
		begin

    @buffers[:string] ||= []
    yield @buffers[:string]
  		end
when 16 then
		begin

    # Append list argument if its value is not empty.
    list_arg_value = @buffers[:list_arg].children[0].children[0]
    if list_arg_value != ''
      @buffers[:list] <<= @buffers[:list_arg]
    end
  		end
when 18 then
		begin

    #TODO: Mark @buffers[:list_arg] string as error.
    @buffers[:list_arg] = s(:list_item, @buffers[:string])
  		end
when 19 then
		begin

    #TODO: Mark @buffers[:list_arg] identifier as error.
    @buffers[:list_arg] = s(:list_item, @buffers[:ident])
  		end
when 21 then
		begin

    @buffers[:list] ||= s(:list)
    yield @buffers[:list]
  		end
		end # eof action switch
	end
	if _trigger_goto
		next
	end
end
	end
	if _goto_level <= _out
		break
	end
	end
	end

      # end: ragel        
          end
        end
      end
    end
  end
end

